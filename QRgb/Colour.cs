namespace QRgb
{
    public class Colour
    {
        #region Fields

        public int R = 0, G = 0, B = 0;

        #endregion Fields

        #region Constructors

        public Colour()
        {
        }

        public Colour(int r, int g, int b)
        {
            R = r;
            G = g;
            B = b;
        }

        #endregion Constructors
    }
}